function backspace() {
    let content = tx1.innerHTML;
    if (content) {
        // Удаляем последний символ или пробел
        tx1.innerHTML = content.replace(/&nbsp;$/, '').slice(0, -1);
    }
}
function del() {
    let content = tx1.innerHTML;
    if (content) {
        // Удаляем последний символ или пробел
        tx1.innerHTML = content.replace(/&nbsp;$/, '').slice(0, 0);
    }
}


